package g_tambahan

/* MAIN PARAMETERS: parameter yang digunakan ketika mengeksekusi main function
    > args: Array<String> => parameter main function
   -> Cara membuatnya dengan menggunakan key intellij (maina)
   -> Untuk memasukkan data melalui Edit Configurations.. > Program arguments
*/
fun main(args: Array<String>) {
    for (i in args) {
        println(i)
    }
}