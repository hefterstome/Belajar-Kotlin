package g_tambahan

/* PACKAGE: tempat yang bisa digunakan dalam meorganisir file.
    > Seperti folder pada explorer
   -> Biasa menggunakan huruf kecil
   -> Untuk membuat sub-package bisa menggunakan tanda titik (.)
    > Contoh: com.company.projek.util

    IMPORT: cara yang dilakukan untuk dapat menggunakan function dari package lain.
    -> import namapacakge.namafunction => import 1 function
    -> import namapacakge.* => import semua function
    -> menuliskan secara lengkap ketika ingin menggunakan function
       Contoh main{
                com.company.projek.sayHello()
              }
 */