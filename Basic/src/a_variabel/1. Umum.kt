package a_Variabel

/*Variable: tempat untuk menyimpan data/value

/*Ketentuan yang dianjurkan
    1. Tidak boleh diawali angka/simbol
       -> 1nama, #umur, @tinggi (Salah)
    2. Menerapkan lower case
       -> Nama, Umur, Tinggi (Salah)
    3. Menerapkan camel case
       -> namaAwal, namaTengah, namaAkhir (Benar)
    4. Menggunakan underscore
       -> nama_awal, nama_tengah, nama_akhir (Benar)
*/

/*Deklarasi Variable
    1. Menuliskan tipe data
       -> val/var namaVariable: TipeData = value
       -> val namaDepan: String = "Iqbra Kurniawan"
    2. Tanpa menuliskan tipe data (Default char(''), string(""), number(int), floating(double))
       -> val/var namaVariable = value
*/

/*Variable dalam kotlin
    1. Mutable (Dapat diubah)
       -> Dengan memakai var
    2. Immutable(Tidak dapat diubah)
        -> Dengan memakai val
*/