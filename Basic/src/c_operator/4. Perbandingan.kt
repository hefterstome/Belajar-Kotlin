package c_Operator

/* Perbandingan: operasi yang digunakan untuk membadingkan dua value
   -> Hasil perbandingan akan bertipe boolean
   -> Jika hasil benar = true
   -> Jika hasil salah = false

** Operator
   1. >  : lebih dari
   2. <  : kurang dari
   3. >= : lebih dari sama dengan
   4. <= : kurang dari sama dengan
   5. == : sama dengan
   6. != : tidak sama dengan
*/
fun main() {

    val a = 1
    val b = 2
    println(a>b)

    val charA = 'A'
    val chara = 'a'
    println(charA==chara)
    println(charA!=chara)

}