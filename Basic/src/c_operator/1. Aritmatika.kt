package c_Operator

/* Aritmatika/matematika
   1. (+) => Penjumlahan
   2. (-) => Pengurangan
   3. (*) => Perkalian
   4. (/) => Pembagian ---> Hasil pembagian pada kotlin akan bernilai integer jika tidak diubah ke floating
   5. (%) => Modulus/sisa bagi
*/
fun main() {
    val pembagian = 10/3
    println(pembagian)
    val pembagianDouble: Double = 10.0/3.0
    println(pembagianDouble)

}
