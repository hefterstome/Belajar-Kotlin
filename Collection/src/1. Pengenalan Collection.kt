/*
 ** COLLECTION: hal umum yang biasa dimiliki oleh bahasa pemrograman

  * Jenis:
    1. Mutable: collection yang mendukung read write (show, add, edit, delete)
    2. Immutable: collection yang mendukung read only

  * Collection dalan kotlin
    Iterable: 1. Collection: tidak bisa diubah -> list, set
              2. Mutable iterable: bisa diubah -> mutable collection -> mutable list, mutable set

   > Dalam Kotlin, default collection adalah immutable
     Hal ini mengindikasikan bahwa tidak ada class immutable
   > Jika ingin membuat collection mutable menambahkan kata mutable
*/